#include <stdio.h>

int main(void)
{
	int i;
	int n;
	int real_n;
	
	scanf("%d", &n);
	real_n = n;
	while (n > 0)
	{
		i = 1;
		while (i <= real_n - n + 1)
		{
			printf("*");
			i++;
		}
		printf("\n");
		n--;
	}
	return (0);
}
